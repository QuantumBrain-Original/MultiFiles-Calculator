#include "sMath.h"
#include <cmath>

double add(double& a, double& b)
{
    return a + b;
}

double sub(double& a, double& b)
{
    return a - b;
}

double mult(double& a, double& b)
{
    return a * b;
}

double div(double& a, double& b)
{
    return a / b;
}

double expPow(double& a, double& b)
{
    return pow(a, b);
}
